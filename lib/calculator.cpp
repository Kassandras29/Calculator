#include "calculator.h"

int sum(int value1, int value2) {

    return value1 + value2;
}

int sub(int value1, int value2) {

    return value1 - value2;
}

int multi(int value1, int value2) {

    return value1 * value2;
}

int divi(int value1, int value2) {

    return value1 / value2;
}
